// ------------------------------------------------------------------
// 原则：
//   - 永远不施压。天空不会骂人，云也不催。
//   - 第二人称（"你"），短句为主，像一个在身边的朋友。
//   - 漏签不追问，长期不炫数字。
//   - 句子里最多 1 个感叹号、1 个 emoji。
//   - 不写死：所有用户可见文案都从这里取，组件只 import，不在 JSX 里写字。
