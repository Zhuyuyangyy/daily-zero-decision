/**
 * 内部 helper：从只读数组里随机取一个元素。
 * 每次调用都重新摇号（无 memo、无 seed），让同一句文案不会连续重复。
 */
function pick<T>(arr: readonly T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

